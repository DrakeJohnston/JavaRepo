    public String FindSpecificFile(String fileName)
    {
        String[] pathNames;
        String file = "";
        File f = new File("F:\\Projects\\FileManager");
        
        FilenameFilter filter = new FilenameFilter() {
     
            @Override
            public boolean accept(File f, String name) {
            return name.startsWith(fileName)&& name.endsWith(".txt");
            }
            
        };
        
        pathNames = f.list(filter);
        
        for (String pathName : pathNames){
        
            file = pathName;
        }  
        
        return file;
    }
    
    public static Object[] readFile(String fileName){
        
        Object persName = new Object();
        Object persJob = new Object();
        int persAge = 0;
        
        try{
            
            FileInputStream fileStream = new FileInputStream(fileName);
            ObjectInputStream objStream = new ObjectInputStream(fileStream);
            
            persName = objStream.readObject();
            persJob = objStream.readObject();
            persAge = objStream.readInt();
            
            objStream.close();      
                                  
            
        }catch(IOException e){
            
            System.out.println("An error occured");
            e.printStackTrace();
            
        }catch (Exception e){   
            
            System.out.println("An error occured");
            e.printStackTrace();
            
        }
        
        return new Object[]{persName, persJob, persAge};
    }
    
