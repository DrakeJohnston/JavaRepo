    private void saveChangeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangeBtnActionPerformed
        // TODO add your handling code here:
        MainFrame mF = new MainFrame();
        Object[] person = readFile(fileSelected);
        
        name = person[0].toString();
        job = person[1].toString();
        age = person[2];
        
        if(newNameField.getText() == "")
        {
            name = person[0].toString();
        }else{
            
            name = newNameField.getText();
        
        }
        
        if(newJobField.getText() == "")
        {
            
            job = person[1].toString();
            
        }else{
            
            job = newJobField.getText();
        
        }
        
        if(newAgeField.getText() == "")
        {
            
            age = person[2];
            
        }else{
            
            age = Integer.parseInt(newAgeField.getText());
        
        }
        
        String fileName = name + "_" + job;
        File f = new File("E:\\Projects\\FileManager2.0\\" + fileSelected);  
        f.delete();
        mF.WriteFile(fileName, name, job, (int) age, true);           
    }//GEN-LAST:event_saveChangeBtnActionPerformed

